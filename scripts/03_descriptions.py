def template_desc(d):
    loc = f"{d['city']}{d['county']}" if d["county"] else d["city"]
    dyn = d["dynasty"]
    ty = d["type"]
    base = f"位于{loc}。{dyn}时期遗存"
    if ty == "古建筑":
        base += "，现存建筑保留了该时期的结构与做法"
    elif ty == "石窟寺及石刻":
        base += "，存窟龛造像石刻"
    elif ty == "古遗址":
        base += "，为重要的考古遗址"
    elif ty == "古墓葬":
        base += "，为重要的古代墓葬"
    else:
        base += "，为近现代重要史迹"
    base += f"。{d['batch']}全国重点文物保护单位（{d['year']}年公布）。"
    return base

def main():
    data = json.load(open(f"{ROOT}/data_work.json", encoding="utf-8"))
    manual = tmpl = 0
    for d in data:
        d.pop("tier", None)
        if d["id"] in DESC:
            d["description"] = DESC[d["id"]]
            d["desc_source"] = "manual"
            manual += 1
        else:
            d["description"] = template_desc(d)
            d["desc_source"] = "template"
            tmpl += 1
    out = f"{ROOT}/shanxi_guobao_final.json"
    json.dump(data, open(out, "w", encoding="utf-8"), ensure_ascii=False, indent=1)
    os.makedirs(f"{ROOT}/src/data", exist_ok=True)
    shutil.copy(out, f"{ROOT}/src/data/buildings.json")
    print(f"手写 {manual} 条 / 模板 {tmpl} 条 -> shanxi_guobao_final.json & src/data/buildings.json")
    miss = [d["id"] for d in data if d.get("lat") is None]
    print("缺坐标:", miss if miss else "无")

if __name__ == "__main__":
    main()
